import static org.junit.Assert.assertEquals;
import static org.junit.Assert.assertNotNull;

import java.io.File;

import javax.xml.parsers.DocumentBuilder;
import javax.xml.parsers.DocumentBuilderFactory;

import org.junit.Test;
import org.w3c.dom.Document;
import org.w3c.dom.Element;
import org.w3c.dom.Node;
import org.w3c.dom.NodeList;

public class BeanValueTest {

	@Test
	public void testBeanValueOfApllicationContext() {
		// on fait ce test pour eviter d'envoyer en prod la mauvaise valeur du
		// bean qui permet de faire usurpation d'identité
		String referenceAttribute = null;

		try {
			File fXmlFile = new File("src/main/webapp/WEB-INF/applicationContext-security.xml");
			DocumentBuilderFactory dbFactory = DocumentBuilderFactory.newInstance();
			DocumentBuilder dBuilder = dbFactory.newDocumentBuilder();
			Document doc = dBuilder.parse(fXmlFile);

			// optional, but recommended
			// read this -
			// http://stackoverflow.com/questions/13786607/normalization-in-dom-parsing-with-java-how-does-it-work
			doc.getDocumentElement().normalize();

			NodeList nList = doc.getElementsByTagName("sec:authentication-provider");

			for (int temp = 0; temp < nList.getLength(); temp++) {
				Node nNode = nList.item(temp);
				if (nNode.getNodeType() == Node.ELEMENT_NODE) {
					Element eElement = (Element) nNode;
					referenceAttribute = eElement.getAttribute("ref");
				}
			}
		} catch (Exception e) {
			e.printStackTrace();
		}
		assertNotNull(referenceAttribute);
		assertEquals("valveAuthenticationProvider", referenceAttribute);
	}

}
